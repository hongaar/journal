#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const webPkgJsonPath = path.join(root, "apps", "web", "package.json");
const outPath = path.join(
  root,
  "apps",
  "web",
  "src",
  "plugins",
  "generated-manifests.ts",
);

/** @param {string} pkgName */
function isPluginPackage(pkgName) {
  return (
    /^@curolia\/plugin-/.test(pkgName) && pkgName !== "@curolia/plugin-contract"
  );
}

const webPkgJson = JSON.parse(fs.readFileSync(webPkgJsonPath, "utf8"));
const deps = Object.keys(webPkgJson.dependencies ?? {})
  .filter(isPluginPackage)
  .sort();

const imports = deps
  .map(
    (pkg, idx) =>
      `import { pluginManifest as pluginManifest${idx} } from "${pkg}";`,
  )
  .join("\n");
const manifests = deps.map((_, idx) => `  pluginManifest${idx},`).join("\n");

const source = `// Auto-generated by scripts/generate-web-plugin-registry.mjs
// Do not edit manually.
import type { PluginPackageManifest } from "@curolia/plugin-contract";
${imports ? `\n${imports}\n` : ""}
export const installedPluginManifests: PluginPackageManifest[] = [
${manifests}
];
`;

fs.writeFileSync(outPath, source, "utf8");
console.log(
  `generated ${path.relative(root, outPath)} with ${deps.length} plugin(s)`,
);
